export const getGearById = (id: string): Gear | undefined => {
  return gears.find((gear) => gear.id === id);
};

export const getGearsByCategory = (category: Gear["category"]): Gear[] => {
  return gears.filter((gear) => gear.category === category);
};

export const getGearsByRarity = (rarity: Gear["rarity"]): Gear[] => {
  return gears.filter((gear) => gear.rarity === rarity);
};

export const getSprinklers = (): Gear[] => {
  return getGearsByCategory("sprinkler");
};

export const getFertilizers = (): Gear[] => {
  return getGearsByCategory("fertilizer");
};

export const getTools = (): Gear[] => {
  return getGearsByCategory("tool");
};

export const getDecorations = (): Gear[] => {
  return getGearsByCategory("decoration");
};
